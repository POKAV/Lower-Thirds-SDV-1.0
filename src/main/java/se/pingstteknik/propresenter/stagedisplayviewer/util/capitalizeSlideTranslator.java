package se.pingstteknik.propresenter.stagedisplayviewer.util;

public class capitalizeSlideTranslator {

    /**
     * Capitalizes all words in every line.
     *
     * @param text
     * @return Capitalized text
     */
    public static String transform(String text) {
        String finalText = text.toUpperCase();
        return finalText;
    }
}